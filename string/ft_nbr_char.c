#include "../includes/libft.h"

int		ft_nbr_char(char *str, char c)
{
	int nbr_c;
	int i;

	i = 0;
	nbr_c = 0;
	while (str[i] != '\0')
	{
		if (str[i] == c)
			nbr_c++;
		i++;
	}
	return (nbr_c);
}

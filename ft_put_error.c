#include "libft.h"

void	ft_put_error(char *str)
{
	int i;
	char c;

	c = '\n';
	i = 0;
	while (str[i] != '\0')
	{
		write(2, &str[i], 1);
		i++;
	}
	write(2, &c, 1);
}
